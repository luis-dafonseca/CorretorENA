class Grades:

    #--------------------------------------------------------------------------#
    def __init__(self):
        self.T = 0
        self.Q = [0] * 30

#------------------------------------------------------------------------------#
class AnswersKey:

    #--------------------------------------------------------------------------#
    def __init__(self, keys):

        if isinstance(keys, str):

            K = ("".join(keys.split())).upper()

            print(K)

            self.keys = []
            for C in K:
                self.keys.append( 'ABCDE'.index(C) )

        else:
            self.keys = keys

    #--------------------------------------------------------------------------#
    def check( self, marks ):

        grades = Grades()
    
        for ii in range(30):
    
            M = marks[ii]
    
            if len(M) != 1:
                N = 0
            elif M[0] == self.keys[ii]:
                N = 1 
            else:
                N = 0
    
            grades.Q[ii] = N
            grades.T    += N
    
        return grades

